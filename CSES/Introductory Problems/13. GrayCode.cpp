vector<string> gray(int n){
    vector<string>a;
    if(n==1){
        a.push_back("0"); a.push_back("1");
        return a;
    }
    else{
        vector<string> b = gray(n-1);
        for(int i = 0;i<b.size();i++){
            string s = "0" + b[i];
            a.push_back(s);
        }
        for(int i = b.size()-1;i>=0;i--){
            string s = "1" + b[i];
            a.push_back(s);
        }
        return a;
    }
}

